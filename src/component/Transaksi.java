    void tampilTabel() {
        try {
            Koneksi objK = new Koneksi();
            Connection con = objK.getConnection();
            Statement st = con.createStatement();
            String sql = "Select * from transaksi order by No_Kwitansi";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                String No_Kwitansi = rs.getString("No_Kwitansi");
                String Tanggal = rs.getString("Tanggal");
                String Kode_A3L = rs.getString("Kode_A3L");
                String Kota = rs.getString("Kota");
                String Telepon = rs.getString("Telepon");
                String Kode_Barang = rs.getString("Kode_Barang");
                String Nama_Barang = rs.getString("Nama_Barang");
                String Harga = rs.getString("Harga");
                String Kode_Pengiriman = rs.getString("Kode_Pengiriman");
                String Tujuan_Pengiriman = rs.getString("Tujuan_Pengiriman");
                String Jml_Barang = rs.getString("Jml_Barang");
                String Jml_Harga = rs.getString("Jml_Harga");
                String HrgPengiriman = rs.getString("HrgPengiriman");
                String[] data = {No_Kwitansi, Tanggal, Kode_A3L, Kota, Telepon, Kode_Barang, Nama_Barang, Harga, Kode_Pengiriman, Tujuan_Pengiriman , Jml_Barang, Jml_Harga, HrgPengiriman};
                tampil.addRow(data);
                
            }
            rs.close();
            con.close();
        } catch (SQLException | HeadlessException e) {

        }
    }


    void autoNumber() {
        try {
            Koneksi objK = new Koneksi();
            Connection con = objK.getConnection();
            Statement st = con.createStatement();
            String sql = "select * from transaksi order by No_Kwitansi DESC";
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                String kd = rs.getString("No_Kwitansi");
                int No_Kwitansi = Integer.parseInt(kd) + 1;
                txtNoKwitansi.setText(String.valueOf(No_Kwitansi));
                rs.close();
                con.close();
            } else {
                txtNoKwitansi.setText("1");
            }
        } catch (Exception e) {
        }

    }

    void hapusTabel() {
        int brs = tampil.getRowCount();
        for (int i = 0; i < brs; i++) {
            tampil.removeRow(0);
        }
    }
    
